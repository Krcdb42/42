int		ft_sqrt(int nb)
{
	int res;

	res = 1;
	if (nb == 1)
		return (1);
	if (nb == 2 || nb == 0)
		return (0);
	while (res <= nb / 2)
	{
		if (res * res == nb)
			return (res);
		res++;
	}
	return (0);
}
